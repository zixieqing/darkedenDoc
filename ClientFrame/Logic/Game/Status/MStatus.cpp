#include "Client_PCH.h"
#include "MStatus.h"

//----------------------------------------------------------------------
//
// constructor / destructor
//
//----------------------------------------------------------------------
MStatus::MStatus()
{
	for (int i=0; i<MAX_MODIFY; i++)
	{
		m_Status[i] = MODIFY_NULL;
	}
	//ClearStatus();

	m_Status[ MODIFY_ADVANCEMENT_CLASS_LEVEL ] = 0;
}

MStatus::~MStatus()
{
}

//----------------------------------------------------------------------
//
// member functions
//
//----------------------------------------------------------------------

//----------------------------------------------------------------------
// ClearStatus
//----------------------------------------------------------------------
// 상태값을 모두 0으로 만든다.
//----------------------------------------------------------------------
void
MStatus::ClearStatus()
{
	for (int i=0; i<MAX_MODIFY; i++)
	{
		m_Status[i] = 0;
	}
}

//----------------------------------------------------------------------
// ApplyStatus
//----------------------------------------------------------------------
// MODIFY_NULL이 아닌 경우만.. 대입한다.
//----------------------------------------------------------------------
void	
MStatus::ApplyStatus(const MStatus& status)
{
	for (int i=0; i<MAX_MODIFY; i++)
	{
		if (status.m_Status[i] != MODIFY_NULL)
		{
			SetStatus( i, status.m_Status[i] );
		}
	}
}
